package pakke1;

public class Rute {
	
	private int verdi;
	private int egenskap;
	
	public Rute(int verdi, int egenskap) {
		this.verdi = verdi;
		this.egenskap = egenskap;
	}

	public int getVerdi() {
		return verdi;
	}

	public void setVerdi(int verdi) {
		this.verdi = verdi;
	}

	public int getEgenskap() {
		return egenskap;
	}

	public void setEgenskap(int egenskap) {
		this.egenskap = egenskap;
	}
	
	
}
